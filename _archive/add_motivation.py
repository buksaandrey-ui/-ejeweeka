    if not os.path.exists(filepath):
        print(f"Skipping {filename}: Not found")
        continue
    
    with open(filepath, "r") as f:
        content = f.read()

    # Avoid double injection
    if "motivation-card" in content:
        print(f"Skipping {filename}: Already injected")
        continue

    # Inject CSS
    content = content.replace("</style>", css_block)

    # Inject HTML
    # We look for </div > or </div> followed by <!-- Bottom Actions -->
    new_html = html_template.replace("{TEXT}", text)
    
    # Try multiple regex patterns since spacing varies
    content = re.sub(r'</div\s*>\s*<!-- Bottom Actions -->', new_html, content)
    
    # If not found, look for </div> followed by Bottom Actions
    
    with open(filepath, "w") as f:
        f.write(content)
        
    print(f"Successfully injected {filename}")
