  // --- Логика ---

  static String _pick(List<String> list) {
    if (list.isEmpty) return "";
    return list[_rand.nextInt(list.length)];
  }

  static String _capitalize(String s) {
    if (s.isEmpty) return s;
    return s[0].toUpperCase() + s.substring(1);
  }

  /// Возвращает персонализированное обращение ("красотка", "бро" и т.д.)
  static String getPersonaLabel(UserProfile p) {
    final isMale = p.gender == 'male';
    
    // Определяем возрастную группу
    final age = p.age ?? 30;
    String ageKey = '';
    if (isMale) {
      if (age < 30) {
        ageKey = 'under_30';
      } else if (age <= 50) ageKey = '30_50';
      else ageKey = 'over_50';
    } else {
      if (age < 30) {
        ageKey = 'under_30';
      } else if (age <= 45) ageKey = '30_45';
      else ageKey = 'over_45';
    }

    // Определяем цель
    String goalKey = 'health'; // default
    if (p.goal == 'lose_weight') {
      goalKey = 'lose_weight';
    } else if (p.goal == 'gain_muscle') goalKey = 'gain_muscle';

    final ageMap = isMale ? _maleAge : _femaleAge;
    final goalsMap = isMale ? _maleGoals : _femaleGoals;

    // С вероятностью 50/50 берем из возрастной группы или из цели
    final useAge = _rand.nextBool();
    
    String word;
    if (useAge && ageMap.containsKey(ageKey)) {
      word = _pick(ageMap[ageKey]!);
    } else if (goalsMap.containsKey(goalKey)) {
      word = _pick(goalsMap[goalKey]!);
    } else {
      word = isMale ? "чемпион" : "красотка";
    }
    
    return word;
  }

  /// Приветствие на Dashboard: "Аня, яркое утречко! Готова сегодня сиять, красотка?"
  static String getGreeting(UserProfile p) {
    final isMale = p.gender == 'male';
    final hour = DateTime.now().hour;
    String timeKey = 'day';
    if (hour >= 5 && hour < 12) {
      timeKey = 'morning';
    } else if (hour >= 12 && hour < 17) timeKey = 'day';
    else if (hour >= 17 && hour < 22) timeKey = 'evening';
    else timeKey = 'night';

    // 1. Имя
    final name = (p.name != null && p.name!.trim().isNotEmpty) ? p.name!.trim() : (isMale ? 'Бро' : 'Подруга');

    // 2. Приветствие (убираем запятые и делаем с маленькой буквы)
    String greeting = _pick(_timeOfDay[timeKey]!).replaceAll(',', '');
    greeting = greeting.toLowerCase();

    // 3. Мотивация
    String timePeriod = (timeKey == 'morning' || timeKey == 'day') ? 'morning_day' : 'evening_night';
    final motivations = isMale ? _maleMotivations[timePeriod]! : _femaleMotivations[timePeriod]!;
    final motivation = _pick(motivations);
    
    // Определяем пунктуацию в конце: если фраза вопросительная или призывная, то ? или !
    bool isQuestion = motivation.startsWith("готова") || 
                      motivation.startsWith("а ты знала") || 
                      motivation.startsWith("идем") || 
                      motivation.startsWith("не сбавляем");
    String punctuation = isQuestion ? "?" : "!";
    final capitalizedMotivation = _capitalize(motivation);

    // 4. Персона
    final persona = getPersonaLabel(p);
    
    return "$name, $greeting! $capitalizedMotivation, $persona$punctuation";
  }

  /// Мотивация после тренировки: "круто позанималась"
  static String getPostWorkout(UserProfile p) {
    final isMale = p.gender == 'male';
    final map = isMale ? _maleActions : _femaleActions;
    return _capitalize(_pick(map['post_workout']!));
  }

  /// Прогресс (для баров прогресса и воды)
  static String getProgressMotivation(UserProfile p, double percent) {
    if (percent >= 1.0) {
      final isMale = p.gender == 'male';
      final map = isMale ? _maleActions : _femaleActions;
      return _capitalize(_pick(map['progress_100']!));
    }
    
    final p100 = (percent * 100).toInt();
    String key;
    if (p100 < 15) {
      key = '0_to_15';
    } else if (p100 < 35) key = '15_to_35';
    else if (p100 < 60) key = '35_to_60';
    else if (p100 < 85) key = '60_to_85';
    else key = '85_to_99';

    return _capitalize(_pick(_progressGeneral[key]!));
  }
