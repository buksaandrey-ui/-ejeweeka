def seed():
    Base.metadata.create_all(bind=engine, tables=[DrugFoodInteraction.__table__])
    db = SessionLocal()

    existing = db.query(DrugFoodInteraction).count()
    if existing > 0:
        print(f"⚠️  Таблица drug_food_interactions уже содержит {existing} записей. Очищаем...")
        db.execute(text("TRUNCATE TABLE drug_food_interactions RESTART IDENTITY"))
        db.commit()

    added = 0
    for r in RULES:
        dfi = DrugFoodInteraction(
            drug_keyword=r["drug_keyword"],
            drug_name=r["drug_name"],
            severity=r["severity"],
            foods_avoid=r["foods_avoid"],
            foods_include=r["foods_include"],
            rule_text=r["rule_text"],
            category=r["category"],
        )
        db.add(dfi)
        added += 1

    db.commit()
    db.close()
    print(f"✅ Добавлено {added} правил лекарство-еда.")


if __name__ == "__main__":
    seed()
