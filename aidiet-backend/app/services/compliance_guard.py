import re

class MedicalRiskGuard:
    """
    Интерцептор для проверки сгенерированного текста (плана или обоснований) 
    на наличие медицинских диагнозов и клеймов. Устраняет риски Apple Guideline 1.4.1.
    """
    
    # Список слов, которые не должны встречаться в "медицинском" контексте
    MEDICAL_TRIGGERS = [
        r'\bлечени[еяю]\b', 
        r'\bлечить\b',
        r'\bдиагноз\b', 
        r'\bзаболевани[ея]\b',
        r'\bболезн[иь]\b', 
        r'\bпрепарат[ы]?\b',
        r'\bлекарств[оа]\b',
        r'\bврач[ауом]?\b',
        r'\bмедицинск[ийоая]\b',
        r'\bтерапи[яи]\b',
        r'\bпациент[а]?\b',
        r'\bэндокринолог[ау]?\b',
        r'\bгастроэнтеролог[ау]?\b'
    ]

    @staticmethod
    def sanitize(text: str) -> str:
        """
        Сканирует текст и заменяет опасные медицинские формулировки на 
        нейтральные wellness-синонимы или вставляет дисклеймер.
        """
        if not text:
            return text
            
        sanitized = text
        
        # Точечные замены
        replacements = {
            r'\bлечени[еяю]\b': 'поддержке',
            r'\bлечить\b': 'поддерживать',
            r'\bдиагноз\b': 'состояние',
            r'\bзаболевани[ея]\b': 'особенности здоровья',
            r'\bболезн[иь]\b': 'особенности',
            r'\bпрепарат[ы]?\b': 'добавки',
            r'\bлекарств[оа]\b': 'средства',
            r'\bпациент[а]?\b': 'пользователь',
        }
        
        for pattern, replacement in replacements.items():
            sanitized = re.sub(pattern, replacement, sanitized, flags=re.IGNORECASE)
            
        return sanitized
    
    @staticmethod
    def add_disclaimer(text: str) -> str:
        """
        Добавляет обязательный дисклеймер в конец текста, если он похож на рекомендацию.
        """
        disclaimer = "\n\n*Примечание: План носит информационный и нутрициологический характер. Не является медицинской рекомендацией.*"
        if disclaimer not in text:
            return text + disclaimer
        return text
